public class Print_Numbers {
    public  static  void main (String[] args){
        //print number 0 to 10 (Increment counter)
        int i = 0;
        System.out.println("Number 0 to 10:");
        while (i <= 10){
            System.out.println(i);
            i++;
        }
        //print number 10 to 0 (Decrement counter)
         int j = 10;
        System.out.println("Number 10 to 0:");
        while (j >= 0){
            System.out.println(j);
            j--;
        }


    }
}
